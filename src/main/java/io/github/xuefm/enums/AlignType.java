package io.github.xuefm.enums;

/**
 * 对齐方式
 */
public class AlignType {


    public enum TransverseAlign {

        /**
         * 左对齐
         */
        LEFT,

        /**
         * 居中
         */
        CENTER,

        /**
         * 右对齐
         */
        RIGHT,


    }

    public enum VerticalAlign {
        /**
         * 顶部对齐
         */
        TOP,

        /**
         * 居中
         */
        CENTER,

        /**
         * 底部对齐
         */
        BOTTOM,

    }


}
